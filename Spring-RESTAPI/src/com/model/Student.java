package com.model;

public class Student {
	private int rollNo;
	private String fName;
	private String lName;
	private int age;
	private String schoolName;
	public Student(int rollNo, String fName, String lName, int age, String schoolName) {
		super();
		this.rollNo = rollNo;
		this.fName = fName;
		this.lName = lName;
		this.age = age;
		this.schoolName = schoolName;
	}
	
	public Student() {
		
	}

}
